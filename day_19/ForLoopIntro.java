package day_19;

public class ForLoopIntro {
    public static void main(String[] args) {
        System.out.println("Hello World");
        System.out.println("Hello World");
        System.out.println("Hello World");
        System.out.println("Hello World");
        System.out.println("Hello World");
        System.out.println("----------------------");

        for(int i = 1; i <= 6;  i++){   //i : 1, 3, 5, 7
            System.out.println("Hello world"); // 3 times
        }

    }
}
