package day03_Variables;

public class Currencies {
    public static void main(String[] args) {
        int dollar = 200;
        double yuan = dollar * 6.68;
        double ruble = dollar * 73.61;
        double euro = dollar * 0.82;

        System.out.print("Chinese Currency:");
        System.out.println(yuan);
        System.out.print("Russian currency:");
        System.out.println(ruble);
        System.out.print("Europe currency:");
        System.out.println(euro);


    }

}
