package day03_Variables;
/*
/n:new line


 */
public class EscapeSequences {
    public static void main(String[] args) {
        System.out.println("Hello everyone \nHow are you today?");
        System.out.println("---------------");
        System.out.println("Hello everyone \n I am living in Brooklyn");
        System.out.println("------------");
        System.out.println("\t Today is a great day");
        System.out.println("-----------------------");
        System.out.println("my favorite music group is Sahro");
        System.out.println("Hi how are guys? \nI am good \n\twhat are you doing now I am watching \"SUITS\"");
        System.out.println("\"perfect\"");


    }



}
