package day04_Variable;

public class VariablePractice {
    public static void main(String[] args) {
        //  int num1= 100, num2 = 10000, num6 = 850000;
        int num1 = 100;
        int num2= 10000;
        double num3 = 3.5;
        double num4 = 2.5f;
        long num5 = 99999999999L;
        int num6 = 850000;
        int lola7 = 20000;

        // float num7 = 5.5; larger primitives can not be assigned to smaller primitives directly

        System.out.println("num1 : " + num1);
        System.out.println("num2 : " + num2);
        System.out.println("num3 : " + num3);
        System.out.println("num4 = " + num4);
        System.out.println("num5 = " + num5);
        System.out.println("num6 = " + num6);
        System.out.println("lola7 : " + lola7);

    }



}
